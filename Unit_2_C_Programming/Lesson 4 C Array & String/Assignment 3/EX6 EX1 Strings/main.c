# include "stdio.h"

int main ()
{
	    char s[100];
		char ch;
        int i,freq=0;
		printf("Enter a string: ");
		fflush(stdout); fflush(stdin);
		gets(s);

		printf("Enter a character to find frequency: ");
				fflush(stdout); fflush(stdin);
				scanf("%c",&ch);
        for(i=0;s[i]!=0;i++)
        {
        	if(ch== s[i])
        		freq++;
        }
        printf("Frequency of %c =%d ",ch,freq);

}
